var (
	sqliteTypeCleanup = regexp.MustCompile(`(?i)\s+PRIMARY\s+KEY\s+AUTOINCREMENT`)
)

// SchemaParts holds table DDL and secondary index DDL separately so imports can
// load data before building indexes (much faster than maintaining indexes per row).
type SchemaParts struct {
	Tables  string
	Indexes string
}

// ConvertSchemaParts converts SQLite DDL into Postgres table and index SQL.
func ConvertSchemaParts(inputPath string) (SchemaParts, int, error) {
	tables, err := ParseDump(inputPath)
	if err != nil {
		return SchemaParts{}, 0, err
	}

	coerceCtx, err := BuildTypeCoercionContext(inputPath, tables)
	if err != nil {
		return SchemaParts{}, 0, err
	}

	indexes, err := ParseIndexes(inputPath)
	if err != nil {
		return SchemaParts{}, 0, err
	}

	var tableBuf strings.Builder
	tableBuf.WriteString("-- Generated by pscale import d1 convert-schema (tables)\n")
	fmt.Fprintf(&tableBuf, "-- Source: %s\n\n", inputPath)

	converted := 0
	tableByName := make(map[string]TableSchema, len(tables))
	for _, table := range tables {
		tableByName[table.Name] = table
	}
	for _, name := range topologicalLoadOrder(tables) {
		table, ok := tableByName[name]
		if !ok {
			continue
		}
		if IsORMMetadataTable(table.Name) {
			continue
		}
		tableBuf.WriteString(convertTableDDL(table, tables, coerceCtx))
		tableBuf.WriteString("\n\n")
		converted++
	}

	var indexBuf strings.Builder
	if len(indexes) > 0 {
		indexBuf.WriteString("-- Generated by pscale import d1 convert-schema (indexes)\n")
		fmt.Fprintf(&indexBuf, "-- Source: %s\n\n", inputPath)
		indexBuf.WriteString("-- Indexes\n")
		for _, idx := range indexes {
			if IsORMMetadataTable(idx.Table) {
				continue
			}
			ddl := convertIndexDDL(idx.RawDDL)
			if ddl == "" {
				continue
			}
			indexBuf.WriteString(ddl)
			indexBuf.WriteString("\n")
		}
		indexBuf.WriteString("\n")
	}

	return SchemaParts{
		Tables:  tableBuf.String(),
		Indexes: indexBuf.String(),
	}, converted, nil
}

// ConvertSchema converts SQLite CREATE TABLE statements to PostgreSQL DDL.
func ConvertSchema(inputPath, outputPath string) (int, error) {
	parts, converted, err := ConvertSchemaParts(inputPath)
	if err != nil {
		return 0, err
	}

	var b strings.Builder
	b.WriteString(parts.Tables)
	if parts.Indexes != "" {
		b.WriteString(parts.Indexes)
	}

	if err := os.WriteFile(outputPath, []byte(b.String()), 0o600); err != nil {
		return 0, fmt.Errorf("write schema: %w", err)
	}

	return converted, nil
}

func convertTableDDL(table TableSchema, all []TableSchema, ctx *TypeCoercionContext) string {
	var b strings.Builder
	fmt.Fprintf(&b, "CREATE TABLE IF NOT EXISTS %s (\n", postgres.QuoteIdentifier(table.Name))

	var lines []string
	for _, col := range table.Columns {
		lines = append(lines, "  "+convertColumn(col, table, all, ctx))
	}
	for _, constraint := range table.Constraints {
		if converted := convertTableConstraint(constraint, table, all); converted != "" {
			lines = append(lines, "  "+converted)
		}
	}
	b.WriteString(strings.Join(lines, ",\n"))
	b.WriteString("\n);\n")

	return b.String()
}

func convertColumn(col ColumnSchema, table TableSchema, all []TableSchema, ctx *TypeCoercionContext) string {
	pgType := sqliteTypeToPostgres(col, table, all, ctx)

	var parts []string
	parts = append(parts, postgres.QuoteIdentifier(col.Name), pgType)

	// Generated (computed) columns cannot have a DEFAULT in Postgres; the generation
	// expression takes its place. Postgres only supports STORED generated columns
	// (pre-18), so VIRTUAL columns are materialized as STORED as the closest
	// equivalent rather than silently dropping the computation.
	if col.GeneratedExpr != "" {
		parts = append(parts, "GENERATED ALWAYS AS ("+convertCheckExpr(col.GeneratedExpr, table)+") STORED")
	}

	if col.AutoIncrement {
		parts = append(parts, "GENERATED BY DEFAULT AS IDENTITY")
		if col.PrimaryKey {
			parts = append(parts, "PRIMARY KEY")
		}
	} else if col.PrimaryKey {
		parts = append(parts, "PRIMARY KEY")
	}

	if col.NotNull && !col.AutoIncrement {
		parts = append(parts, "NOT NULL")
	}

	if col.Unique {
		parts = append(parts, "UNIQUE")
	}

	if col.DefaultValue != "" && !col.AutoIncrement && col.GeneratedExpr == "" {
		parts = append(parts, "DEFAULT", convertDefault(col.DefaultValue, pgType))
	}

	for _, check := range col.CheckExprs {
		parts = append(parts, "CHECK ("+convertCheckExpr(check, table)+")")
	}

	if col.ForeignKey != "" {
		parts = append(parts, convertReferencesClause(col.ForeignKey, all))
	}

	return strings.Join(parts, " ")
}

func sqliteTypeToPostgres(col ColumnSchema, table TableSchema, all []TableSchema, ctx *TypeCoercionContext) string {
	if isUUIDColumn(col, table, all, ctx) {
		return "UUID"
	}

	t := strings.ToUpper(col.Type)

	if col.AutoIncrement {
		if strings.Contains(t, "BIG") {
			return "BIGINT"
		}
		return "INTEGER"
	}

	switch {
	case t == "":
		return "TEXT"
	case strings.Contains(t, "INT"):
		if isBooleanLikeColumn(col, table, ctx) {
			return "BOOLEAN"
		}
		return "BIGINT"
	case strings.Contains(t, "CHAR") || strings.Contains(t, "CLOB") || strings.Contains(t, "TEXT"):
		if isJSONText(col) && ctx != nil && samplesAllowJSON(table.Name, col.Name, ctx) {
			return "JSONB"
		}
		if isTimestampText(col) && ctx != nil && samplesAllowTimestamp(table.Name, col.Name, ctx) {
			return "TIMESTAMPTZ"
		}
		return "TEXT"
	case strings.Contains(t, "BLOB"):
		return "BYTEA"
	case strings.Contains(t, "REAL") || strings.Contains(t, "FLOA") || strings.Contains(t, "DOUB"):
		return "DOUBLE PRECISION"
	case strings.Contains(t, "BOOL"):
		return "BOOLEAN"
	case strings.Contains(t, "NUMERIC") || strings.Contains(t, "DECIMAL"):
		return numericPostgresType(t)
	default:
		return "TEXT"
	}
}

// numericPostgresType maps a SQLite NUMERIC/DECIMAL affinity type (already upper-cased) to
// a Postgres NUMERIC type, preserving any declared precision/scale, e.g. "DECIMAL(10,2)" ->
// "NUMERIC(10,2)".
func numericPostgresType(t string) string {
	idx := strings.Index(t, "(")
	if idx < 0 {
		return "NUMERIC"
	}
	end := strings.Index(t[idx:], ")")
	if end < 0 {
		return "NUMERIC"
	}
	return "NUMERIC" + t[idx:idx+end+1]
}

func convertDefault(def, pgType string) string {
	def = strings.TrimSpace(def)
	upper := strings.ToUpper(def)
	if upper == "NULL" {
		return "NULL"
	}
	if mapped := mapSQLiteDefaultFunction(def, pgType); mapped != "" {
		return mapped
	}
	if mapped := mapSQLiteCastDefault(def, pgType); mapped != "" {
		return mapped
	}
	if pgType == "BOOLEAN" {
		if lit := parseBooleanDefaultLiteral(def); lit != "" {
			return lit
		}
	}
	if pgType == "UUID" {
		return "'" + strings.Trim(def, "'\"") + "'"
	}
	if strings.HasPrefix(def, "'") {
		// Already a valid (SQLite and Postgres share '' escaping) single-quoted literal.
		return def
	}
	if strings.HasPrefix(def, `"`) {
		// SQLite's double-quoted string-literal fallback: Postgres always treats double
		// quotes as identifiers, so re-quote as a proper single-quoted string literal.
		return quotePostgresLiteral(unwrapDoubleQuotedLiteral(def))
	}
	if pgType == "TEXT" || pgType == "TIMESTAMPTZ" {
		return quotePostgresLiteral(strings.Trim(def, "'\""))
	}
	return def
}

// quotePostgresLiteral wraps s as a Postgres single-quoted string literal, doubling any
// embedded single quotes so the result is always syntactically valid.
func quotePostgresLiteral(s string) string {
	return "'" + strings.ReplaceAll(s, "'", "''") + "'"
}

// unwrapDoubleQuotedLiteral strips the surrounding double quotes from SQLite's
// double-quoted string-literal fallback syntax (e.g. `"active"`), un-escaping any doubled
// internal quotes. Returns s unchanged if it isn't double-quoted.
func unwrapDoubleQuotedLiteral(s string) string {
	if len(s) >= 2 && strings.HasPrefix(s, `"`) && strings.HasSuffix(s, `"`) {
		inner := s[1 : len(s)-1]
		return strings.ReplaceAll(inner, `""`, `"`)
	}
	return s
}

// unwrapOuterParens repeatedly strips a single matching outer paren pair that wraps the
// entire expression, e.g. "((random()))" -> "random()".
func unwrapOuterParens(s string) string {
	for {
		s = strings.TrimSpace(s)
		if !strings.HasPrefix(s, "(") {
			return s
		}
		end, ok := matchingParenEnd(s, 0)
		if !ok || end != len(s)-1 {
			return s
		}
		s = s[1:end]
	}
}

// isBareFunctionCall reports whether s (after stripping any wrapping parens) is exactly a
// no-argument call to the named SQLite function, e.g. isBareFunctionCall("(random())", "RANDOM").
func isBareFunctionCall(s, name string) bool {
	unwrapped := strings.ToUpper(unwrapOuterParens(s))
	return unwrapped == strings.ToUpper(name)+"()"
}

// parseBooleanDefaultLiteral maps a SQLite integer DEFAULT literal to a Postgres boolean
// literal using SQLite truthiness (0 is false, any other integer is true).
func parseBooleanDefaultLiteral(def string) string {
	if n, err := strconv.ParseInt(strings.TrimSpace(def), 10, 64); err == nil {
		if n == 0 {
			return "FALSE"
		}
		return "TRUE"
	}
	return ""
}

// looksLikeUUIDGeneratorExpr detects SQLite's common "fake UUID" idiom built from
// randomblob()/hex() (e.g. lower(hex(randomblob(4))) || '-' || ...).
func looksLikeUUIDGeneratorExpr(def string) bool {
	return strings.Contains(strings.ToUpper(def), "RANDOMBLOB(")
}

// randomBytesExpr returns a Postgres expression producing n pseudo-random bytes using only
// built-in functions (no pgcrypto dependency). n must be positive.
func randomBytesExpr(n int) string {
	blocks := (n + 15) / 16
	parts := make([]string, blocks)
	for i := 0; i < blocks; i++ {
		parts[i] = fmt.Sprintf("decode(md5(random()::text || clock_timestamp()::text || %d::text), 'hex')", i)
	}
	joined := strings.Join(parts, " || ")
	if blocks > 1 {
		joined = "(" + joined + ")"
	}
	return fmt.Sprintf("substring(%s from 1 for %d)", joined, n)
}

// randomblobArgN extracts the byte-count argument from a bare `randomblob(N)` call, or
// returns 0 if s isn't a randomblob call with a literal integer argument.
func randomblobArgN(s string) int {
	trimmed := strings.TrimSpace(s)
	if !strings.HasPrefix(strings.ToUpper(trimmed), "RANDOMBLOB(") {
		return 0
	}
	arg := sqliteFunctionArg(trimmed, "RANDOMBLOB")
	n, err := strconv.Atoi(strings.TrimSpace(arg))
	if err != nil {
		return 0
	}
	return n
}

func mapSQLiteDefaultFunction(def, pgType string) string {
	trimmed := strings.TrimSpace(def)
	upper := strings.ToUpper(trimmed)
	switch upper {
	case "CURRENT_TIMESTAMP", "(CURRENT_TIMESTAMP)":
		return "CURRENT_TIMESTAMP"
	case "CURRENT_DATE", "(CURRENT_DATE)":
		return "CURRENT_DATE"
	case "CURRENT_TIME", "(CURRENT_TIME)":
		return "CURRENT_TIME"
	}
	if strings.HasPrefix(upper, "DATETIME(") || strings.HasPrefix(upper, "(DATETIME(") {
		return "now()"
	}
	if strings.HasPrefix(upper, "DATE(") || strings.HasPrefix(upper, "(DATE(") {
		return "CURRENT_DATE"
	}
	if strings.HasPrefix(upper, "TIME(") || strings.HasPrefix(upper, "(TIME(") {
		if pgType == "TEXT" {
			return "to_char(now(), 'HH24:MI:SS')"
		}
		return "CURRENT_TIME"
	}
	if arg := sqliteFunctionArg(trimmed, "UNIXEPOCH"); arg != "" || strings.HasSuffix(upper, "UNIXEPOCH()") {
		if mapped := mapUnixEpochDefault(arg, pgType); mapped != "" {
			return mapped
		}
	}
	if arg := sqliteFunctionArg(trimmed, "JULIANDAY"); arg != "" || strings.HasSuffix(upper, "JULIANDAY()") {
		modifier := strings.ToUpper(strings.Trim(strings.TrimSpace(arg), `'"`))
		if modifier == "" || modifier == "NOW" {
			return "(extract(epoch from now()) / 86400.0 + 2440587.5)"
		}
	}
	if pgType == "UUID" && looksLikeUUIDGeneratorExpr(trimmed) {
		return "gen_random_uuid()"
	}
	if (pgType == "BIGINT" || pgType == "INTEGER") && isBareFunctionCall(trimmed, "RANDOM") {
		return "(floor(random() * 9223372036854775807))::bigint"
	}
	unwrapped := unwrapOuterParens(trimmed)
	if n := randomblobArgN(unwrapped); n > 0 {
		return randomBytesExpr(n)
	}
	if n := randomblobArgN(sqliteFunctionArg(unwrapped, "HEX")); n > 0 {
		return "encode(" + randomBytesExpr(n) + ", 'hex')"
	}
	return ""
}

// mapSQLiteCastDefault recognizes `CAST(expr AS type)` DEFAULT expressions and, when the
// inner expr is a recognized SQLite function call, maps it to a Postgres equivalent
// producing the requested target type instead of silently becoming a literal string.
func mapSQLiteCastDefault(def, pgType string) string {
	trimmed := unwrapOuterParens(def)
	upper := strings.ToUpper(trimmed)
	if !strings.HasPrefix(upper, "CAST(") {
		return ""
	}
	end, ok := matchingParenEnd(trimmed, 4)
	if !ok {
		return ""
	}
	inner := trimmed[5:end]
	asIdx := indexOutsideQuotes(strings.ToUpper(inner), " AS ")
	if asIdx < 0 {
		return ""
	}
	innerExpr := strings.TrimSpace(inner[:asIdx])
	castType := strings.ToUpper(strings.TrimSpace(inner[asIdx+len(" AS "):]))

	var innerPgType string
	wantText := false
	switch {
	case strings.Contains(castType, "INT"):
		innerPgType = "BIGINT"
	case strings.Contains(castType, "REAL") || strings.Contains(castType, "FLOA") || strings.Contains(castType, "DOUB"):
		innerPgType = "DOUBLE PRECISION"
	case strings.Contains(castType, "CHAR") || strings.Contains(castType, "TEXT") || strings.Contains(castType, "CLOB"):
		innerPgType = "BIGINT"
		wantText = true
	default:
		return ""
	}

	mapped := mapSQLiteDefaultFunction(innerExpr, innerPgType)
	if mapped == "" {
		return ""
	}
	if wantText {
		return "(" + mapped + ")::text"
	}
	return mapped
}

func mapUnixEpochDefault(arg, pgType string) string {
	modifier := strings.ToUpper(strings.Trim(strings.TrimSpace(arg), `'"`))
	switch modifier {
	case "", "NOW":
		return unixEpochNowDefault(pgType)
	case "SUBSEC":
		return unixEpochSubsecDefault(pgType)
	}
	if pgType == "TIMESTAMPTZ" && unixEpochArgLooksNumeric(arg) {
		return "to_timestamp(" + strings.TrimSpace(arg) + ")"
	}
	if (pgType == "BIGINT" || pgType == "INTEGER" || pgType == "DOUBLE PRECISION") && unixEpochArgLooksNumeric(arg) {
		return strings.TrimSpace(arg)
	}
	return ""
}

func unixEpochNowDefault(pgType string) string {
	switch pgType {
	case "TIMESTAMPTZ":
		return "now()"
	case "BIGINT", "INTEGER":
		return "extract(epoch from now())::bigint"
	case "DOUBLE PRECISION":
		return "extract(epoch from now())"
	default:
		return "now()"
	}
}

func unixEpochSubsecDefault(pgType string) string {
	switch pgType {
	case "TIMESTAMPTZ":
		return "clock_timestamp()"
	case "BIGINT", "INTEGER":
		return "extract(epoch from clock_timestamp())::bigint"
	case "DOUBLE PRECISION":
		return "extract(epoch from clock_timestamp())"
	default:
		return "clock_timestamp()"
	}
}

func unixEpochArgLooksNumeric(arg string) bool {
	arg = strings.TrimSpace(arg)
	if arg == "" {
		return false
	}
	unquoted := strings.Trim(arg, `'"`)
	if _, err := strconv.ParseFloat(unquoted, 64); err == nil {
		return true
	}
	return !strings.HasPrefix(arg, "'") && !strings.HasPrefix(arg, `"`)
}

func sqliteFunctionArg(s, fn string) string {
	fnUpper := strings.ToUpper(fn)
	idx := strings.Index(strings.ToUpper(s), fnUpper+"(")
	if idx < 0 {
		return ""
	}
	start := idx + len(fnUpper) + 1
	depth := 1
	for i := start; i < len(s); i++ {
		switch s[i] {
		case '(':
			depth++
		case ')':
			depth--
			if depth == 0 {
				return strings.TrimSpace(s[start:i])
			}
		}
	}
	return ""
}

// ConvertCreateStatement converts a raw SQLite CREATE TABLE line to Postgres (for tests).
func ConvertCreateStatement(sqliteDDL string) string {
	ddl := sqliteTypeCleanup.ReplaceAllString(sqliteDDL, "")
	ddl = regexp.MustCompile(`(?i)\bAUTOINCREMENT\b`).ReplaceAllString(ddl, "")
	ddl = regexp.MustCompile(`(?i)\bINTEGER\b`).ReplaceAllStringFunc(ddl, func(s string) string {
		return "BIGINT"
	})
	ddl = regexp.MustCompile(`(?i)\bREAL\b`).ReplaceAllString(ddl, "DOUBLE PRECISION")
	return ddl
}
